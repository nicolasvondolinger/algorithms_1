#include <bits/stdc++.h>
using namespace std;

#define _ ios_base::sync_with_stdio(0); cin.tie(0);
#define endl '\n'
#define ff first
#define ss second
#define pb push_back

typedef long long ll;

const int INF = 0x3f3f3f3f;
const ll LINF = 0x3f3f3f3f3f3f3f3fll;
ll n1 = 0, n2 = 0;

/*!
 * @class       Path
 *              Represents a path in the graph.
 * @field       destiny     Destination node of the path.
 * @field       finalYear   Year when the path becomes available.
 * @field       crossTime   Time to cross the path.
 * @field       cost        Cost to use the path.
 */
class Path{
    public:
        ll destiny;
        ll finalYear;
        ll crossTime;
        ll cost;
};

/*!
 * @brief       Prints the elements of the cost vector.
 * @param       cost    Vector containing the costs.
 */
void print(vector<ll>& cost){
    for(unsigned i = 0; i < cost.size(); i++){
        cout << cost[i] << endl;
    }
}

/*!
 * @brief       Implementation of Dijkstra's algorithm to find shortest paths.
 * @param       graph   Adjacency list of the graph.
 * @return      Vector containing shortest path costs from the source.
 */
vector<ll> dijkstra(vector<vector<Path>>& graph){
    priority_queue<pair<ll, ll>> pq;
    vector<ll> ans(graph.size(), LINF);

    vector<ll> times(graph.size(), LINF);
    times[0] = 0;

    pq.push(make_pair(-0, 0));
    ans[0] = 0;

    while(!pq.empty()){
        ll current = pq.top().ss;
        pq.pop();
        for(Path x: graph[current]){
            ll v = x.destiny, w = x.crossTime;
            if(ans[v] > ans[current] + w){
                ans[v] = ans[current] + w;
                pq.push(make_pair(-ans[v], v));
                times[v] = x.finalYear;
            }
        }
    }

    for(auto t: times) n1 = max(n1, t);

    return ans;
}

/*!
 * @brief       Calculates the minimum spanning tree (MST) cost using Prim's algorithm.
 * @param       graph   Adjacency list of the graph.
 * @return      Total cost of the MST.
 */
ll minimun_spanning_tree(vector<vector<Path>>& graph){
    ll t = 0;
    vector<bool> visited(graph.size(), false);
    vector<vector<pair<ll, ll>>> ng(graph.size());
    for(ll i = 0; i < graph.size(); i++){
        for(ll j = 0; j < graph[i].size(); j++){
            ng[i].pb(make_pair(graph[i][j].cost, graph[i][j].destiny));
        }
    }
    priority_queue<pair<ll, ll>, vector<pair<ll, ll>>, greater<pair<ll, ll>>> pq;
    pq.push({0, 0});
    while(!pq.empty()){
        auto p = pq.top(); pq.pop();
        ll c = p.ff, u = p.ss;
        if(visited[u]) continue;
        t += c; visited[u] = true;
        for(auto v: ng[u]){
            if(!visited[v.ss]){
                pq.push(v);
            }
        }
    }

    return t;
}

/*!
 * @brief       Depth-first search (DFS) to mark reachable nodes within a certain year limit.
 * @param       graph   Adjacency list of the graph.
 * @param       visited Vector to track visited nodes.
 * @param       n       Current node.
 * @param       max     Year limit for traversing the paths.
 */
void dfs(vector<vector<Path>>& graph, vector<bool>& visited, ll n, ll max){
    visited[n] = true;

    for(auto w: graph[n]) if(!visited[w.destiny] && w.finalYear <= max){
        dfs(graph, visited,  w.destiny, max);
    }
}

/*!
 * @brief       Main solving function that reads input and performs graph analysis.
 */
void solve(){
    ll n, m; cin >> n >> m;
    vector<vector<Path>> graph(n); 
    Path p1, p2;
    set<ll> years;
    ll u, v, a, l, c;
    for(int i = 0; i < m; i++){
        cin >> u >> v >> a >> l >> c; u--, v--; 
        years.insert(a);
        p1.destiny = v; p1.finalYear = a; p1.crossTime = l; p1.cost = c;
        p2.destiny = u; p2.finalYear = a; p2.crossTime = l; p2.cost = c;
        graph[u].pb(p1); graph[v].pb(p2);
    }

    vector<ll> new_years;

    for(auto y: years) new_years.pb(y);

    ll left = 0, right = new_years.size() - 1;
    while (left <= right){
        ll count = 0, mid = left + (right - left)/2, year = new_years[mid];
        vector<bool> visited(n, false);
        for(ll j = 0; j < n; j++) if(!visited[j]){
            if(count == 2) break;
            dfs(graph, visited, j, year);
            count++;
        }
        if(count == 1){
            n2 = year;
            right = mid - 1;
        } else {
            left = mid + 1;
        }
    }

    vector<ll> cost = dijkstra(graph);
    print(cost); // Hours
    cout << n1 << endl; // Mutually Realizable Distances
    cout << n2 << endl; // Year from which the graph is connected
    cout << minimun_spanning_tree(graph) << endl; // Minimum cost to connect the kingdom
}

int main(){ 
    int t = 1;
    while(t--) solve();
    exit(0);
}
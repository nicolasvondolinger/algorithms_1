    ll mdc(ll a, ll b){
        if(a%b == 0) return b;
        else return mdc(b, a%b);
    }

    ll mmc(ll a, ll b){
        ll r, n, m; n = a, m = b;
        do{
            r = n % m;
            n = m;
            m = r;
        } while(r != 0);
        return (a*b)/ n;
    }

